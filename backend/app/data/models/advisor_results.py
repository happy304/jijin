"""ORM model for the ``advisor_results`` table.

Stores trading advice results generated by the advisor engine,
allowing users to review historical advice without re-running analysis.

Each row represents one complete advisor session (request + response).
"""

from __future__ import annotations

from datetime import date, datetime

from sqlalchemy import BigInteger, Date, DateTime, Integer, JSON, Numeric, String, Text, func
from sqlalchemy.dialects.postgresql import JSONB
from sqlalchemy.orm import Mapped, mapped_column

from app.data.models import Base

# Use JSONB on PostgreSQL, fall back to JSON on other dialects
_JsonType = JSON().with_variant(JSONB, "postgresql")
_IdType = Integer().with_variant(BigInteger, "postgresql")


class AdvisorResult(Base):
    """Persistent representation of an advisor analysis result.

    Stores both the request parameters and the generated advice,
    so users can review past results without re-running analysis.
    """

    __tablename__ = "advisor_results"

    id: Mapped[int] = mapped_column(
        _IdType,
        primary_key=True,
        autoincrement=True,
        comment="Result unique identifier",
    )

    # 请求参数
    advice_date: Mapped[date] = mapped_column(
        Date,
        nullable=False,
        index=True,
        comment="Date the advice was generated",
    )
    fund_codes: Mapped[list] = mapped_column(
        _JsonType,
        nullable=False,
        comment="List of fund codes analyzed",
    )
    total_capital: Mapped[float] = mapped_column(
        Numeric(20, 2),
        nullable=False,
        comment="Total capital used for analysis",
    )
    risk_level: Mapped[str] = mapped_column(
        String(20),
        nullable=False,
        comment="Risk level: conservative/moderate/aggressive",
    )
    strategy_id: Mapped[int | None] = mapped_column(
        _IdType,
        nullable=True,
        comment="Strategy ID if portfolio mode",
    )
    strategy_name: Mapped[str | None] = mapped_column(
        String(100),
        nullable=True,
        comment="Strategy name if portfolio mode",
    )
    current_positions: Mapped[dict | None] = mapped_column(
        _JsonType,
        nullable=True,
        comment="Current positions at time of analysis",
    )
    positions_detail: Mapped[dict | None] = mapped_column(
        _JsonType,
        nullable=True,
        comment="Position details (amount, buy_date, cost)",
    )
    user_profile: Mapped[dict | None] = mapped_column(
        _JsonType,
        nullable=True,
        comment="User investment profile at time of analysis",
    )
    learned_params_version_id: Mapped[int | None] = mapped_column(
        _IdType,
        nullable=True,
        index=True,
        comment="Resolved learned-parameter version id used during analysis",
    )
    parameter_set_id: Mapped[str | None] = mapped_column(
        String(64),
        nullable=True,
        index=True,
        comment="Resolved governed default parameter set id used during analysis",
    )
    source_result_id: Mapped[int | None] = mapped_column(
        _IdType,
        nullable=True,
        index=True,
        comment="Source advisor result id when generated from refresh or replay",
    )
    analysis_mode: Mapped[str | None] = mapped_column(
        String(30),
        nullable=True,
        comment="Execution mode: live/portfolio/history_refresh/nightly/walk_forward",
    )
    execution_context: Mapped[dict | None] = mapped_column(
        _JsonType,
        nullable=True,
        comment="Serialized execution context and audit metadata",
    )

    # 结果
    advices: Mapped[list] = mapped_column(
        _JsonType,
        nullable=False,
        comment="Full advice list (serialized TradingAdvice objects)",
    )
    summary: Mapped[dict] = mapped_column(
        _JsonType,
        nullable=False,
        comment="Summary statistics",
    )

    # 用户备注
    note: Mapped[str | None] = mapped_column(
        Text,
        nullable=True,
        comment="User note/memo for this result",
    )

    # v3: 建议执行跟踪
    tracked_returns: Mapped[dict | None] = mapped_column(
        _JsonType,
        nullable=True,
        comment="Tracked actual returns after advice: {fund_code: {return_5d, return_10d, return_20d, return_60d, hit_20d}}",
    )
    tracked_at: Mapped[datetime | None] = mapped_column(
        DateTime(timezone=True),
        nullable=True,
        comment="Last tracking update timestamp",
    )

    created_at: Mapped[datetime | None] = mapped_column(
        DateTime(timezone=True),
        nullable=True,
        server_default=func.now(),
        comment="Creation timestamp (UTC)",
    )
    updated_at: Mapped[datetime | None] = mapped_column(
        DateTime(timezone=True),
        nullable=True,
        server_default=func.now(),
        onupdate=func.now(),
        comment="Last update timestamp (UTC)",
    )

    def __repr__(self) -> str:
        return (
            f"<AdvisorResult id={self.id!r} date={self.advice_date!r} "
            f"funds={len(self.fund_codes)} risk={self.risk_level!r}>"
        )
